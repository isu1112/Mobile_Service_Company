    public void autoGenerateID(){
    
    Connection con = database.connection();
    
    try{
    
        Statement stmt = con.createStatement();
        
        ResultSet result = stmt.executeQuery("select id from employee order by id desc limit 1");
        
        if(result.next()){
            
           String userID =  result.getString(1);
           
           userID = userID.substring(1, 4);
           
           int intID = Integer.parseInt(userID);
           
           intID++;
           
           if( 10 > intID){
               lblEmployeeId.setText("E00" + intID);           
           }else if (100 > intID ){
               lblEmployeeId.setText("E0" + intID);
           }else{
               lblEmployeeId.setText("E" + intID);
           }
                 
        }else{
            lblEmployeeId.setText("E001");
        
        }
        
    
    }catch(Exception e){
    e.printStackTrace();
    
    }
    }
    
