    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {
                
            Connection con = database.connection();
            
            PreparedStatement ptst = con.prepareStatement("insert into employee (id,eName,eAge,eEmail,eAddress,eNIC,contact,eDesignation,eDepartment)values(?,?,?,?,?,?,?,?,?)");
            ptst.setString(1, lblEmployeeId.getText());
            ptst.setString(2, txtName.getText());
            ptst.setInt(3, Integer.parseInt(txtAge.getText()));
            ptst.setString(4, txtEmail.getText());
            ptst.setString(5, txtAddress.getText());
            ptst.setString(6, txtNic.getText());
            ptst.setInt(7, Integer.parseInt(txtContact.getText()));
            ptst.setString(8, cmbDesignation.getSelectedItem().toString());
            ptst.setString(9, cmbDepartment.getSelectedItem().toString());
            
            int i = ptst.executeUpdate();
            
            
            if(i != 0){
                JOptionPane.showMessageDialog(null, "Successfully Added!");
                clear();
            
            }else{
                JOptionPane.showMessageDialog(null, "Something Error !");
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

